package things;

public class Animal {
	
	private String name;
	private int age;
	private boolean iswild;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public boolean isIswild() {
		return iswild;
	}
	public void setIswild(boolean iswild) {
		this.iswild = iswild;
	}
	
	

}
